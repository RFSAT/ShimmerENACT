package com.rfsat.shimmerenact.data.repository

import android.content.Context
import android.location.Location
import android.os.Environment
import com.rfsat.shimmerenact.data.models.RateConstraints
import com.rfsat.shimmerenact.data.models.ShimmerSample
import com.rfsat.shimmerenact.data.models.ShimmerSignal
import kotlinx.coroutines.Dispatchers
import kotlinx.coroutines.withContext
import java.io.BufferedWriter
import java.io.File
import java.io.FileWriter
import java.text.SimpleDateFormat
import java.util.*

// ─── Per-signal recording channel ─────────────────────────────────────────────

private data class SignalChannel(
    val signal: ShimmerSignal,
    val targetHz: Int,         // desired output rate
    val hardwareHz: Int,       // hardware stream rate
    val writer: BufferedWriter,
    val file: File
) {
    // Emit every Nth hardware sample to achieve targetHz
    val decimationStep: Int = (hardwareHz.toDouble() / targetHz.coerceAtLeast(1))
        .coerceAtLeast(1.0).toInt()
    var counter: Int = 0
    var rowsWritten: Long = 0L
}

// ─── Session metadata ─────────────────────────────────────────────────────────

data class RecordingSession(
    val sessionId: String,          // timestamp-based, same for all files in the session
    val deviceName: String,
    val startTimeMs: Long,
    val files: List<RecordingFile>  // one per signal
)

// ─── Repository ───────────────────────────────────────────────────────────────

class RecordingRepository(private val context: Context) {

    private val channels = java.util.concurrent.CopyOnWriteArrayList<SignalChannel>()
    private var sessionId: String = ""
    private var sessionDir: File? = null
    @Volatile private var _isRecording = false
    private var _totalSamplesWritten = 0L

    private val sessionDateFmt = SimpleDateFormat("yyyy-MM-dd_HH-mm-ss", Locale.US)
    private val isoFmt = SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss.SSS'Z'", Locale.US).apply {
        timeZone = TimeZone.getTimeZone("UTC")
    }

    val isRecording: Boolean get() = _isRecording
    val totalSamplesWritten: Long get() = _totalSamplesWritten
    val currentSessionId: String get() = sessionId
    val currentSessionDir: String get() = sessionDir?.absolutePath ?: ""

    // ─── Start recording — one file per selected signal ───────────────────────
    suspend fun startRecording(
        deviceName: String,
        signals: List<ShimmerSignal>,       // signals to record (already filtered to selection)
        signalRatesHz: Map<String, Int>,    // key → target Hz; missing = hardwareHz
        hardwareHz: Int
    ): Result<List<String>> = withContext(Dispatchers.IO) {
        if (_isRecording) stopRecording()
        try {
            sessionId = sessionDateFmt.format(Date())
            val safeName = deviceName.replace(Regex("[^A-Za-z0-9_-]"), "_")

            // Create a session sub-directory so all files from one recording session
            // are grouped together:  ShimmerENACT / session_YYYY-MM-DD_HH-mm-ss /
            val rootDir = getRootDir()
            val dir = File(rootDir, "${safeName}_$sessionId").also { it.mkdirs() }
            sessionDir = dir

            val startTs = isoFmt.format(Date())
            val paths = mutableListOf<String>()

            for (sig in signals) {
                val targetHz = (signalRatesHz[sig.key] ?: hardwareHz).coerceIn(1, hardwareHz)
                val safeKey = sig.key.replace(Regex("[^A-Za-z0-9_]"), "_")
                val file = File(dir, "${safeKey}.csv")
                val bw = BufferedWriter(FileWriter(file))

                // Header block
                bw.write("# RFSAT Limited — ENACT Project (Horizon Europe Grant 101157151)\n")
                bw.write("# Device: $deviceName\n")
                bw.write("# Signal: ${sig.displayName}  [${sig.unit}]\n")
                bw.write("# Hardware rate: $hardwareHz Hz  |  Output rate: $targetHz Hz\n")
                bw.write("# Session start: $startTs\n")
                bw.write("# Generated by ShimmerENACT v${com.rfsat.shimmerenact.BuildConfig.VERSION_NAME}\n")
                bw.write("timestamp_iso,timestamp_ms,${sig.key}_${sig.unit.replace("/", "per")},latitude_deg,longitude_deg,altitude_m,location_accuracy_m\n")

                channels.add(SignalChannel(sig, targetHz, hardwareHz, bw, file))
                paths.add(file.absolutePath)
            }

            _isRecording = true
            _totalSamplesWritten = 0L
            AppLog.ok("REC", "Session started — ${signals.size} files in ${dir.name}")
            Result.success(paths)
        } catch (e: Exception) {
            AppLog.e("REC", "startRecording failed: ${e.message}")
            channels.forEach { runCatching { it.writer.close() } }
            channels.clear()
            Result.failure(e)
        }
    }

    // ─── Write a hardware sample — applies per-channel decimation ─────────────
    fun writeSampleSync(sample: ShimmerSample, location: Location?) {
        if (!_isRecording || channels.isEmpty()) return
        val isoTs = isoFmt.format(Date(sample.timestampMs))
        // Build location columns once — empty strings when no fix available
        val locCols = if (location != null)
            ",${"%.8f".format(location.latitude)},${"%.8f".format(location.longitude)},${"%.3f".format(location.altitude)},${"%.2f".format(location.accuracy.toDouble())}"
        else
            ",,,,"
        var wrote = false
        for (ch in channels) {
            val v = sample.values[ch.signal.key] ?: continue
            ch.counter++
            if (ch.counter >= ch.decimationStep) {
                ch.counter = 0
                try {
                    ch.writer.write("$isoTs,${sample.timestampMs},${"%.6f".format(v)}$locCols\n")
                    ch.rowsWritten++
                    wrote = true
                } catch (_: Exception) {
                    // Writer closed mid-flight (stop race) — ignore silently
                }
            }
        }
        if (wrote) {
            _totalSamplesWritten++
            if (_totalSamplesWritten % 200L == 0L) {
                channels.forEach { runCatching { it.writer.flush() } }
            }
        }
    }

    /** Emergency reset — call if the app detects recording stuck in bad state. */
    fun resetRecordingState() {
        channels.forEach { runCatching { it.writer.close() } }
        channels.clear()
        _isRecording = false
        _totalSamplesWritten = 0L
        AppLog.w("REC", "Recording state forcibly reset")
    }

    // ─── Stop recording ───────────────────────────────────────────────────────
    suspend fun stopRecording(): Result<RecordingSession> = withContext(Dispatchers.IO) {
        _isRecording = false  // Stop writes immediately, even if rest fails
        return@withContext try {
            val endTs = isoFmt.format(Date())
            val files = mutableListOf<RecordingFile>()
            val channelsCopy = channels.toList()  // snapshot before clear
            channels.clear()

            for (ch in channelsCopy) {
                try {
                    ch.writer.write("# Session end: $endTs\n")
                    ch.writer.write("# Rows written: ${ch.rowsWritten}\n")
                    ch.writer.flush()
                    ch.writer.close()
                    if (ch.file.exists() && ch.file.length() > 0) {
                        files.add(RecordingFile(
                            name = ch.file.nameWithoutExtension,
                            path = ch.file.absolutePath,
                            sizeBytes = ch.file.length(),
                            lastModified = ch.file.lastModified(),
                            signalDisplayName = ch.signal.displayName,
                            signalUnit = ch.signal.unit,
                            rateHz = ch.targetHz,
                            sessionId = sessionId,
                            rowCount = ch.rowsWritten
                        ))
                    }
                } catch (e: Exception) {
                    AppLog.e("REC", "Error closing ${ch.signal.key}: ${e.message}")
                }
            }

            val session = RecordingSession(
                sessionId = sessionId,
                deviceName = sessionDir?.name?.substringBeforeLast("_") ?: "Unknown",
                startTimeMs = runCatching {
                    sessionDateFmt.parse(sessionId)?.time ?: 0L
                }.getOrDefault(0L),
                files = files
            )
            AppLog.ok("REC", "Session stopped — ${files.size} files, $_totalSamplesWritten rows")
            Result.success(session)
        } catch (e: Exception) {
            AppLog.e("REC", "stopRecording exception: ${e.javaClass.simpleName}: ${e.message}")
            channels.clear()
            Result.failure(e)
        }
    }

    // ─── List sessions ────────────────────────────────────────────────────────
    suspend fun listSessions(): List<RecordingSession> = withContext(Dispatchers.IO) {
        val root = try {
            getRootDir()
        } catch (e: Exception) {
            AppLog.e("REC", "listSessions: cannot get root dir: ${e.message}")
            return@withContext emptyList()
        }

        AppLog.d("REC", "Scanning: ${root.absolutePath}")

        val allEntries = root.listFiles()
        if (allEntries == null) {
            AppLog.w("REC", "listSessions: listFiles() returned null (no permission?)")
            return@withContext emptyList()
        }

        val sessionDirs  = allEntries.filter { it.isDirectory }
        val legacyFiles  = allEntries.filter { it.isFile && it.extension == "csv" }

        AppLog.d("REC", "Found ${sessionDirs.size} dir(s), ${legacyFiles.size} root CSV(s)")

        val results = mutableListOf<RecordingSession>()

        // ── Session sub-directories (v1.1+) ───────────────────────────────────
        // Directory name format written by startRecording():
        //   {safeName}_{yyyy-MM-dd_HH-mm-ss}
        // The timestamp portion is exactly 19 characters and always at the end.
        for (dir in sessionDirs.sortedByDescending { it.lastModified() }) {
            try {
                val csvFiles = (dir.listFiles() ?: emptyArray())
                    .filter { it.isFile && it.extension == "csv" }
                    .sortedBy { it.name }
                    .mapNotNull { f ->
                        try { parseRecordingFile(f, dir.name) }
                        catch (e: Exception) {
                            AppLog.w("REC", "Cannot parse ${f.name}: ${e.message}")
                            null
                        }
                    }

                AppLog.d("REC", "  dir=${dir.name}  csvs=${csvFiles.size}")

                // Include even empty directories so the user sees them
                val startMs = if (dir.name.length >= 19) {
                    val candidate = dir.name.takeLast(19)   // yyyy-MM-dd_HH-mm-ss
                    runCatching { sessionDateFmt.parse(candidate)?.time }.getOrNull()
                } else null

                // Device name: strip the trailing _yyyy-MM-dd_HH-mm-ss (20 chars = '_' + 19)
                val deviceName = if (dir.name.length > 20)
                    dir.name.dropLast(20).trimEnd('_').replace('_', ' ')
                else dir.name

                results.add(RecordingSession(
                    sessionId  = dir.name,
                    deviceName = deviceName,
                    startTimeMs = startMs ?: dir.lastModified(),
                    files      = csvFiles
                ))
            } catch (e: Exception) {
                AppLog.e("REC", "Error scanning dir ${dir.name}: ${e.message}")
            }
        }

        // ── Bare CSV files in root (v1.0 — no sub-directory) ─────────────────
        if (legacyFiles.isNotEmpty()) {
            AppLog.d("REC", "Legacy root CSVs: ${legacyFiles.size}")
            val legacyCsvs = legacyFiles
                .sortedBy { it.name }
                .mapNotNull { f ->
                    try { parseRecordingFile(f, "legacy") }
                    catch (e: Exception) {
                        AppLog.w("REC", "Cannot parse legacy ${f.name}: ${e.message}")
                        null
                    }
                }
            if (legacyCsvs.isNotEmpty()) {
                results.add(RecordingSession(
                    sessionId   = "legacy",
                    deviceName  = "Legacy recordings",
                    startTimeMs = legacyFiles.minOf { it.lastModified() },
                    files       = legacyCsvs
                ))
            }
        }

        AppLog.i("REC", "listSessions complete: ${results.size} session(s)")
        results.sortedByDescending { it.startTimeMs }
    }

    /**
     * Parse a single CSV file into a RecordingFile.
     * Reads header metadata only (first 30 lines) — safe for large files.
     * Uses UTF-8 with replacement so non-UTF-8 bytes from older device locales
     * do not throw a MalformedInputException.
     */
    private fun parseRecordingFile(f: File, sessionId: String): RecordingFile? {
        if (!f.exists() || !f.canRead()) return null
        return try {

        var rate       = 0
        var rows       = -1L
        var sigName    = f.nameWithoutExtension
        var sigUnit    = ""
        var dataLines  = 0L

        // Stream the file line by line; stop scanning metadata after 30 lines
        // but continue counting data rows until EOF.
        // Use a replacement-mode CharsetDecoder so malformed bytes in files written
        // by older app versions (which used the device default charset via FileWriter)
        // are silently replaced with '?' rather than throwing MalformedInputException.
        val decoder = Charsets.UTF_8.newDecoder()
            .onMalformedInput(java.nio.charset.CodingErrorAction.REPLACE)
            .onUnmappableCharacter(java.nio.charset.CodingErrorAction.REPLACE)
        java.io.BufferedReader(java.io.InputStreamReader(f.inputStream(), decoder)).use { br ->
            var lineNum = 0
            var headerSeen = false
            for (rawLine in br.lineSequence()) {
                lineNum++
                val line = rawLine.trim()
                if (line.isEmpty()) continue

                if (line.startsWith("#")) {
                    // Only parse metadata from the first 30 comment lines
                    if (lineNum <= 30) {
                        when {
                            line.contains("Output rate:") && rate == 0 ->
                                rate = line.substringAfter("Output rate:").trim()
                                    .substringBefore(" Hz").substringBefore("|").trim()
                                    .toIntOrNull() ?: 0
                            line.contains("Hardware rate:") && rate == 0 ->
                                rate = line.substringAfter("Hardware rate:").trim()
                                    .substringBefore(" Hz").substringBefore("|").trim()
                                    .toIntOrNull() ?: 0
                            line.startsWith("# Signal:") -> {
                                sigName = line.substringAfter("# Signal:").trim()
                                    .substringBefore("[").trim()
                                    .ifBlank { f.nameWithoutExtension }
                                sigUnit = line.substringAfter("[", "").substringBefore("]").trim()
                            }
                            line.startsWith("# Rows written:") ->
                                rows = line.substringAfter(":").trim().toLongOrNull() ?: -1L
                        }
                    }
                    continue
                }

                // First non-comment line is the CSV column header
                if (!headerSeen) { headerSeen = true; continue }

                // Count data rows (used only if "# Rows written:" not found)
                dataLines++
            }
        }

        val rowCount = if (rows >= 0) rows else dataLines

        RecordingFile(
            name              = f.nameWithoutExtension,
            path              = f.absolutePath,
            sizeBytes         = f.length(),
            lastModified      = f.lastModified(),
            signalDisplayName = sigName,
            signalUnit        = sigUnit,
            rateHz            = rate,
            sessionId         = sessionId,
            rowCount          = rowCount
        )
        } catch (e: Exception) {
            AppLog.w("REC", "parseRecordingFile(${f.name}): ${e.javaClass.simpleName}: ${e.message}")
            null
        }
    }

    // ─── Delete session directory ─────────────────────────────────────────────
    suspend fun deleteSession(sessionId: String): Boolean = withContext(Dispatchers.IO) {
        if (sessionId == "legacy") {
            // Delete all bare CSV files in root
            val root = getRootDir()
            var ok = true
            root.listFiles { f -> f.isFile && f.extension == "csv" }?.forEach { f ->
                if (!f.delete()) ok = false
            }
            return@withContext ok
        }
        getRootDir().listFiles { f -> f.isDirectory && f.name == sessionId }
            ?.firstOrNull()?.deleteRecursively()
            ?: getRootDir().listFiles { f -> f.isDirectory && f.name.contains(sessionId) }
                ?.firstOrNull()?.deleteRecursively()
            ?: false
    }

    // ─── Delete single file ───────────────────────────────────────────────────
    suspend fun deleteFile(path: String): Boolean = withContext(Dispatchers.IO) {
        File(path).delete()
    }

    // ─── Storage location ──────────────────────────────────────────────────────
    // App-specific external directory — requires no storage permission on API 29+,
    // and only WRITE_EXTERNAL_STORAGE (legacy, max API 28) on Android 9 and below.
    // Files live at: Android/data/com.rfsat.shimmerenact/files/Documents/ShimmerENACT/
    // They are visible in any file manager and fully shareable via the share sheet.
    private fun getRootDir(): File {
        val base = context.getExternalFilesDir(Environment.DIRECTORY_DOCUMENTS)
            ?: context.filesDir   // fallback to internal storage if external unavailable
        val root = File(base, "ShimmerENACT")
        if (!root.exists()) root.mkdirs()
        return root
    }
}

// ─── Data classes ─────────────────────────────────────────────────────────────

data class RecordingFile(
    val name: String,
    val path: String,
    val sizeBytes: Long,
    val lastModified: Long,
    val signalDisplayName: String = name,
    val signalUnit: String = "",
    val rateHz: Int = 0,
    val sessionId: String = "",
    val rowCount: Long = 0L
) {
    val sizeDisplay: String get() = when {
        sizeBytes > 1_048_576 -> "%.1f MB".format(sizeBytes / 1_048_576.0)
        sizeBytes > 1024 -> "%.1f KB".format(sizeBytes / 1024.0)
        else -> "$sizeBytes B"
    }
}
